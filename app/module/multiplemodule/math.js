function add(n1, n2) {

  return n1 + n2;
}

function subtract(n1, n2) {

  return n1 - n2;
}

function multiply(n1, n2) {

  return n1 * n2;
}

function divide(n1, n2) {

  return n1 / n2;
}

function mod(n1, n2) {

  return n1 % n2;
}

let pi = 3.14;

export {
  add, subtract, multiply, divide, mod, pi
}